    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        // TODO add your handling code here:
        Proyecto p = new Proyecto();
        p.setCodigo_proyecto(codigo_proyecto.getText());
        p.setNombre_proyecto(nombre_proyecto.getText());
        p.setRUC(ruc.getText());
        String e = estado.getSelectedItem().toString();
        p.setEstado(e);
        p.setFecha_inicio(fecha_inicio.getText());
        p.setFecha_fin(fecha_fin.getText());
        p.setMonto(Integer.parseInt(monto.getText()));
        String proto = prototipo.getSelectedItem().toString();
        p.setPrototipo(proto);
        if(!"".equals(codigo_proyecto.getText()))
        {
            int Acction = JOptionPane.showOptionDialog(this, "Esta seguro de actualizar los datos? ", "Mensaje", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opActualizar, opActualizar[0]);
            if (Acction == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, "Datos Actualizados ");
                ac.UpdateProyecto(p);
            } else {
                JOptionPane.showMessageDialog(this, "Se cancelo la operacion","Mensaje",JOptionPane.CANCEL_OPTION);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Busca datos en la DB primero","Mensaje",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_actualizarActionPerformed

