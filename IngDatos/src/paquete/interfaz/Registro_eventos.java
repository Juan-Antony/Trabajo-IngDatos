    private void buscar_rucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar_rucActionPerformed
        // TODO add your handling code here:
        LimpiarFormulario();
        evento = me.ListEventoRUC(ruc.getText());
        for(Evento e: evento)
        {
            modelo.addRow(new Object[]{e.getCodigo_evento(),e.getCodigo_proyecto(),e.getNombre_proyecto(),e.getRUC(),e.getNombre_empresa(),e.getFecha(),e.getDescripcion()});
        }
    }//GEN-LAST:event_buscar_rucActionPerformed

