    private void registrar_eventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar_eventoActionPerformed
        // TODO add your handling code here:
        Evento e = new Evento();
        e.setCodigo_evento(cod_evento.getText());
        e.setCodigo_proyecto(cod_proyecto.getText());
        e.setFecha(fecha.getText());
        e.setDescripcion(descripcion.getText());
        if(!"".equals(cod_proyecto.getText()) && !"".equals(cod_evento.getText()))
        {
            db.registrarEvento(e);
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Falta ingresar datos");
        }
    }//GEN-LAST:event_registrar_eventoActionPerformed

